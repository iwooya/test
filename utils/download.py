# ============================================================================
# download.py - 파일 다운로드 유틸리티 (프로그레스 바 포함)
# ============================================================================
# 역할: URL로부터 파일을 다운로드하면서 tqdm 프로그레스 바를 표시한다.
# 호출: genie-dl_improved.py의 download_track() 함수에서 사용
#
# tqdm 프로그레스 바 출력 형식:
#   1. 삐딱하게 (Crooked): 100%|██████████████| 30.7M/30.7M [00:05<00:00, 5.73MiB/s]
#   │                       │    │              │             │      │      │
#   │                       │    │              │             │      │      └─ 다운로드 속도
#   │                       │    │              │             │      └─ 남은 예상 시간 (ETA)
#   │                       │    │              │             └─ 경과 시간 (elapsed)
#   │                       │    │              └─ 전체 파일 크기
#   │                       │    └─ 현재까지 받은 크기
#   │                       └─ 시각적 프로그레스 바 (█ 블록)
#   └─ 곡 이름 (desc 파라미터)
#
#   [00:05<00:00] 의미:
#     00:05 = 지금까지 5초 걸렸다 (elapsed time)
#     00:00 = 남은 시간 0초, 즉 다운로드 완료 (estimated time remaining)
#     둘 다 00:00이면 → 매우 빠르게 1초 미만에 완료되었다는 뜻
# ============================================================================

from tqdm import tqdm       # 터미널 프로그레스 바 라이브러리
import requests             # HTTP 요청 (스트리밍 다운로드)
from pathlib import Path    # 파일 경로 처리 (존재 여부 확인)


def download(url, file_path='', file_name=None, name='Download progress', block_size=1024):
	"""
	[함수] download() - URL에서 파일을 스트리밍 방식으로 다운로드한다.
	
	매개변수:
	  url        : 다운로드할 파일의 URL (지니 스트리밍 서버 URL)
	  file_path  : 저장할 디렉토리 경로 (빈 문자열이면 현재 디렉토리)
	  file_name  : 저장할 파일명 (None이면 URL의 마지막 경로를 파일명으로 사용)
	  name       : 프로그레스 바에 표시할 곡 이름 (desc 파라미터)
	  block_size : 한 번에 읽어올 데이터 크기 (바이트 단위, 기본 1024=1KB)
	              genie-dl에서는 512로 호출함 → 더 세밀한 프로그레스 업데이트
	
	반환값:
	  file_path : 저장된 파일의 전체 경로 (이미 존재하면 None 반환)
	"""

	# ──────────────────────────────────────────────────────────────────────
	# [1] 중복 다운로드 방지: 같은 파일이 이미 있으면 건너뛴다
	# ──────────────────────────────────────────────────────────────────────
	if Path(file_name).is_file():
		print("%s: File already exists. Skipping" % name)
	else:
		# ──────────────────────────────────────────────────────────────────
		# [2] 곡 이름이 너무 길면 60자로 자른다 (터미널 레이아웃 깨짐 방지)
		# 예: "아주 긴 곡 제목이 있는 리믹스 버전 feat. 누구누구..." → 60자 + "..."
		# ──────────────────────────────────────────────────────────────────
		if len(name) > 60:
			name = name[:60] + "..."

		# ──────────────────────────────────────────────────────────────────
		# [3] HTTP 스트리밍 요청 시작
		# ──────────────────────────────────────────────────────────────────
		# stream=True: 응답 본문을 즉시 다운로드하지 않고, iter_content()로
		#              조각(chunk) 단위로 가져온다. 대용량 파일에 필수.
		#              (stream=False면 전체 파일을 메모리에 올린 후 처리)
		resp = requests.get(url, stream=True)

		# content-length 헤더에서 전체 파일 크기(바이트)를 가져온다.
		# 서버가 이 헤더를 안 주면 0 (프로그레스 바가 % 대신 용량만 표시됨)
		total_size_in_bytes = int(resp.headers.get('content-length', 0))

		# ──────────────────────────────────────────────────────────────────
		# [4] tqdm 프로그레스 바 생성
		# ──────────────────────────────────────────────────────────────────
		# total          : 전체 크기 (바이트) → 100% 기준
		# unit='iB'      : 단위 표시를 "iB" (binary bytes)로 설정
		# unit_scale=True : 자동으로 KiB, MiB, GiB 등으로 스케일링
		#                   (30,700,000 bytes → "30.7MiB"로 표시)
		# dynamic_ncols=False : 터미널 폭 변경에 반응하지 않음 (고정 폭)
		# desc           : 프로그레스 바 왼쪽에 표시할 텍스트 (곡 이름)
		# ncols=80       : 프로그레스 바 전체 너비를 80자로 고정
		bar = tqdm(
			total=total_size_in_bytes,
			unit='iB',
			dynamic_ncols=False,
			unit_scale=True,
			desc=name,
			ncols=80
		)

		# ──────────────────────────────────────────────────────────────────
		# [5] 파일명 결정: file_name이 없으면 URL에서 추출
		# ──────────────────────────────────────────────────────────────────
		# 예: "https://app-android.genie.co.kr/.../101514417_48.flac?token=..."
		#     → URL.split('/')[-1] → "101514417_48.flac?token=..." (주의: 쿼리스트링 포함됨)
		# genie-dl에서는 항상 file_name을 지정하므로 이 분기는 거의 안 탐
		if not file_name:
			file_name = url.split('/')[-1]

		# ──────────────────────────────────────────────────────────────────
		# [6] 최종 저장 경로 조합
		# ──────────────────────────────────────────────────────────────────
		# file_path가 있으면 디렉토리 + 파일명으로 조합
		# file_path가 빈 문자열이면 파일명만 사용 (현재 디렉토리에 저장)
		if file_path != '':
			if file_path[-1] == '/':
				file_path = file_path + file_name
			else:
				file_path = file_path + '/' + file_name
		else:
			file_path = file_name

		# ──────────────────────────────────────────────────────────────────
		# [7] 파일 다운로드 실행 (chunk 단위로 쓰기)
		# ──────────────────────────────────────────────────────────────────
		# 'wb' = write + binary 모드 (음원 파일은 바이너리)
		# iter_content(block_size): block_size 바이트씩 서버에서 읽어옴
		#   block_size=512면 → 512바이트(0.5KB)씩 읽음
		#   장점: 프로그레스 바가 더 부드럽게 업데이트됨
		#   단점: I/O 호출 횟수가 늘어나 약간 느릴 수 있음
		with open(file_path, 'wb') as file:
			for data in resp.iter_content(block_size):
				bar.update(len(data))    # 프로그레스 바에 받은 바이트 수 반영
				file.write(data)         # 디스크에 기록
		bar.close()  # 프로그레스 바 종료 (줄바꿈 처리)

		return file_path